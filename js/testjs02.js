var i = 12;
var j = 4;
console.log(i + j); //16 (somme)

i = "Bonjour";
j = "Jean-Claude";
console.log(i + j); // BonjourJean-Claude (concaténation)

/** Conclusion : en fonction du type de données, les opérateurs s'adatent
 * + pour des nombre permet d'additionner
 * + pour des chaînes de caractères permet de concaténer
 */
i = "12";
j = "4";
console.log(i + j); // 12Jean-Claude